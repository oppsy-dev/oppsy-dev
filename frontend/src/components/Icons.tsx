import type { NotificationChannelType } from '../api/notification_channels';

export const CHANNEL_ICON_COLOR: Record<NotificationChannelType, string> = {
  Discord: '#5865F2',
  Webhook: '#71717a',
  Email: '#059669',
};

export const CHANNEL_ICON_BG: Record<NotificationChannelType, string> = {
  Discord: 'rgba(88, 101, 242, 0.08)',
  Webhook: 'rgba(113, 113, 122, 0.08)',
  Email: 'rgba(5, 150, 105, 0.08)',
};

export function ChannelIcon({
  type,
  width,
  height,
}: {
  type: NotificationChannelType;
  width: number;
  height: number;
}) {
  const color = CHANNEL_ICON_COLOR[type];
  const icon =
    type === 'Discord' ? (
      <DiscordIcon width={width} height={height} />
    ) : type === 'Email' ? (
      <EmailIcon width={width} height={height} />
    ) : (
      <WebhookIcon width={width} height={height} />
    );
  return <span style={{ color, display: 'contents' }}>{icon}</span>;
}

